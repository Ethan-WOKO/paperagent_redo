CREATE TABLE sandbox_execution_outbox (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    execution_id VARCHAR(64) NOT NULL UNIQUE,
    plan_id BIGINT NOT NULL,
    step_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    session_id BIGINT NOT NULL,
    project_id BIGINT NOT NULL,
    lease_fence BIGINT NOT NULL,
    idempotency_key VARCHAR(128) NOT NULL,
    request_digest VARCHAR(64) NOT NULL,
    project_version VARCHAR(64) NOT NULL,
    policy_digest VARCHAR(64) NOT NULL,
    request_json CLOB,
    status VARCHAR(32) NOT NULL,
    broker_execution_id VARCHAR(64),
    receipt_digest VARCHAR(64),
    receipt_json CLOB,
    error_code VARCHAR(64),
    dispatch_attempts INT NOT NULL DEFAULT 0,
    next_attempt_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    claim_owner VARCHAR(128), claim_token VARCHAR(64), claim_fence BIGINT NOT NULL DEFAULT 0,
    claim_expires_at TIMESTAMP, retry_phase VARCHAR(16) NOT NULL DEFAULT 'DISPATCH',
    CONSTRAINT uk_sandbox_outbox_idempotency UNIQUE (plan_id, idempotency_key),
    CONSTRAINT uk_sandbox_outbox_step UNIQUE (plan_id, step_id)
);
CREATE INDEX idx_sandbox_outbox_dispatch ON sandbox_execution_outbox(status, next_attempt_at);
